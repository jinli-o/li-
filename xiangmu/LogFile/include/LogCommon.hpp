// ================= 头文件保护 =================
// 防止该头文件被同一个 .cpp 文件重复包含，避免“重复定义”错误
#ifndef LOGCOMMON_HPP
#define LOGCOMMON_HPP

namespace logfile
{
    // ================= 日志级别枚举 =================
    // enum class：C++11 引入的“强类型枚举”
    // 优势：
    // 1. 不会隐式转换为 int（更安全）
    // 2. 作用域限定在 LOG_LEVEL 内（避免命名污染）
    enum class LOG_LEVEL
    { 
        TRACE = 0,  // 最低级别：最详细的追踪信息（如函数入口/出口、变量值变化）
        DEBUG,      // 调试级别：用于开发调试的信息（如中间计算结果）
        INFO,       // 信息级别：一般运行信息（如服务启动、配置加载完成）
        WARN,       // 警告级别：潜在问题，但不影响程序继续运行（如内存不足预警）
        ERROR,      // 错误级别：发生错误，但程序仍可继续运行（如网络请求失败）
        FATAL,      // 致命级别：严重错误，程序必须终止（如内存访问越界、断言失败）
        NUM_LOG_LEVEL // 哨兵值：表示日志级别的总数，方便遍历或数组大小定义
    };

    // ================= 日志级别转字符串数组声明 =================
    // 作用：将枚举值 LOG_LEVEL 转换为人类可读的字符串，用于日志输出
    // 注意：数组顺序必须与 enum class LOG_LEVEL 的定义顺序完全一致！
    extern const char *LLtoStr[];

    // ================= 缓冲区大小常量 =================
    // 作用：定义不同场景下的日志缓冲区大小，平衡内存占用和性能
    static const int SMALL_BUFF_SIZE=128;   // 小缓冲区：用于短日志（如简单的 INFO 信息），节省内存
    static const int MEDIAN_BUFF_SIZE=512;  // 中等缓冲区：用于一般长度的日志（如带参数的 WARN 信息）
    static const int LARGE_BUFF_SIZE=1024;  // 大缓冲区：用于长日志（如带堆栈的 ERROR/FATAL 信息），减少内存扩容次数
}
#endif // 结束头文件保护